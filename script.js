function downloadPDF(pdfPath, title) {
    // Create a temporary link element
    const link = document.createElement('a');
    link.href = pdfPath;
    link.download = title + '.pdf';
    link.target = '_blank';
    
    // Trigger download
    document.body.appendChild(link);
    link.click();
    document.body.removeChild(link);
}

