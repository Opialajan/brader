        function animateStats() {
            const stats = [
                { id: 'stat1', target: 150, suffix: '+' },
                { id: 'stat2', target: 25, suffix: '+' },
                { id: 'stat3', target: 25, suffix: '+' },
                { id: 'stat4', target: 2, suffix: '+' }
            ];

            stats.forEach((stat, index) => {
                let current = 0;
                const increment = stat.target / 50;
                const timer = setInterval(() => {
                    current += increment;
                    if (current >= stat.target) {
                        current = stat.target;
                        clearInterval(timer);
                    }
                    document.getElementById(stat.id).textContent = Math.round(current) + stat.suffix;
                }, 30);
            });
        }

